//named function method
function addition(x,y) {
    return x + y;
}

function subtraction(x,y) {
    return x - y;
}
function multiply(x,y) {
    return x * y;
}

function division(x,y) {
    return x / y;
}


//anonymous function method
var addition2 = function(x,y){
    return x + y;
};

var subtraction2 = function(x,y){
    return x - y;
};

var multiply2 = function(x,y){
    return x * y;
};

var division2 = function(x,y){
    return x / y;
};